namespace XeniaLauncher
{
    public class OptionsStart : IStartEffects
    {
        public void Start(Game1 game, Window window)
        {
            // Volume text
            window.extraSprites[1].ToTextSprite().text = "" + SoundEffect.MasterVolume * 10;
            // Theme preview texture
            window.extraSprites[3].ToObjectSprite().textures[0] = game.themeThumbnails[game.theme.ToString().ToLower()];

            window.buttonEffects.SetupEffects(game, window);
        }
    }
